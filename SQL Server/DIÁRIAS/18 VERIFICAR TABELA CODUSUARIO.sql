USE HUBDADOS;

SELECT  A.IDCNT, A.CODIGOCONTRATO, B.IDPRD, B.PRECOFATURAMENTO, A.VALORCONTRATO FROM CorporeRM.TCNT A
INNER JOIN CorporeRM.TITMCNT B
ON A.IDCNT = B.IDCNT
LEFT JOIN CorporeRM.TITMCNTADITIVO C
ON A.IDCNT = C.IDCNT
WHERE A.CODIGOCONTRATO LIKE '%MAR0.000080.25%'

SELECT IDMOV, IDPRD, FORMAT(PRECOUNITARIO, 'C', 'PT-BR') AS 'VALOR', FORMAT(QUANTIDADE, 'C', 'PT-BR') AS 'QUANTIDADE', DATAEMISSAO FROM CorporeRM.TITMMOV
WHERE IDMOV = '5182037'