USE HUBDADOS;

SELECT F.IDMOV, A.IDCNT, A.CODIGOCONTRATO, B.IDPRD, B.PRECOFATURAMENTO, A.VALORCONTRATO, E.QUANTIDADE, E.PRECOUNITARIO, G.NOME, G.NOMEFANTASIA FROM CorporeRM.TCNT A
INNER JOIN CorporeRM.TITMCNT B
ON A.IDCNT = B.IDCNT
LEFT JOIN CorporeRM.TITMCNTADITIVO C
ON A.IDCNT = C.IDCNT
LEFT JOIN CorporeRM.FCFO D
ON A.CODCFO COLLATE Latin1_General_CI_AI = D.CODCFO 
INNER JOIN CorporeRM.TITMMOV E
ON E.IDMOV = E.IDMOV
RIGHT JOIN CorporeRM.TMOV F
ON E.IDMOV = F.IDMOV
INNER JOIN CorporeRM.FCFO G
ON F.CODCFO = G.CODCFO
WHERE A.CODIGOCONTRATO LIKE '%MAR0.000017.25%' AND F.IDMOV = '5152625'