USE HUBDADOS;

SELECT  A.IDCNT, A.CODIGOCONTRATO, B.IDPRD, B.PRECOFATURAMENTO, A.VALORCONTRATO FROM CorporeRM.TCNT A
INNER JOIN CorporeRM.TITMCNT B
ON A.IDCNT = B.IDCNT
LEFT JOIN CorporeRM.TITMCNTADITIVO C
ON A.IDCNT = C.IDCNT
LEFT JOIN CorporeRM.FCFO D
ON A.CODCFO COLLATE Latin1_General_CI_AI = D.CODCFO 
WHERE A.CODIGOCONTRATO LIKE '%SCA0.000044.25%'

